let number = parseInt(prompt("Ingrese un Número"));

if (number >= 100){
    alert('Su número es: ' + number + ' y es mayor o igual que 100')    
}else if (number < 100){
    alert('Su número es: ' + number + ' y es menor que 100');
}else {
    alert("Lo que usted ingreso no es un Numero. Por favor re-cargue la pagina e ingrese un valor")
}

